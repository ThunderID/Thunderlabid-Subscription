use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;

class Feature extends Model implements \Thunderlabid\Libraries\Pattern\Contract\Model\IModel
{
	use SoftDeletes;

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// VARIABLES
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	protected $table    = 'SUBSC_features';
	public $timestamps  = true;
	protected $fillable = [
		'plan_id', 
		'app_id', 
		'max_tenant', 
		'max_user', 
		'scopes', 
	];

	protected $hidden = [
	];

	protected $casts = [
		'scopes'	=> 'array',
	];

	protected $dates = [
		'deleted_at'
	];

	protected $touches = [
	];

	/*=====================================
	=            CONFIGURATION            =
	=====================================*/
	const APP_ID = ['BASIL_POS'];
	/*=====  End of CONFIGURATION  ======*/
	


	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// CONSTRUCT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// RELATIONSHIP
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// BOOT
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// STATIC FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// FUNCTION
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public function getRules()
	{
		return [
			'plan_id'      	=> ['required', 'string'],
			'app_id'      	=> ['required', 'in:' . implode(',', SELF::APP_ID)],
			'max_tenant' 	=> ['required', 'number'],
			'max_user' 		=> ['required', 'number'],
		];
	}

	public function getRelationships()
	{
		return [];
	}

	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	// ACCESSOR
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	// --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
